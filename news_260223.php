                <p>
                   「2026台北燈節」將於2月25日至3月15日在花博展區與西門展區盛大登場，臺北市政府觀光傳播局表示，今年活動期間展區及周邊禁止吸菸（含電子煙及加熱菸），另有設置吸菸區，違規者將處新臺幣2,000元至1萬元罰鍰，提醒民眾留意禁菸時段規定，並配合禁菸措施，共同維護清新的賞燈環境。
                </p>

                <p>
                    觀傳局表示，2026台北燈節「西門展區」禁菸範圍為開封街以南、長沙街以北、西臨西寧南路、東至延平南路所圍之區域，含中山堂廣場之人行通道及騎樓，以及孝字號天橋上方路面。「花博展區」的禁菸範圍則是花博公園圓山園區及周邊場域，東側至中山北路3段、南側至民族西路，西側含捷運圓山站1號出口往南至線形公園、捷運圓山站2號出口往北至庫倫街口、北側至草坪休憩區所圍區域，並均含人行通道。
                </p>

                <p>
                    觀傳局指出，本次燈節在雙展區皆規劃吸菸區，「西門展區」設在中華路一段北端與南端，並於峨眉立體停車場前亦有規劃一處。「花博展區」除既有MAJI集食行樂商場內的固定吸菸區外，另於花博公園中山北路入口處增設一處。有吸菸需求民眾請至指定吸菸區使用，以免違規受罰。
                </p>

                 <p>
                    觀傳局說明，台北燈節開展期間為2月25日至3月15日，每週一至週五下午5時至晚上10時，每週六日及國定假日下午2時至晚上10時，另「花博展區」於每周六延長亮燈至晚上12時，台北燈節活動期間展區及周邊禁止吸菸，違規者將處新臺幣2,000元至1萬元罰鍰。觀傳局提醒民眾留意禁菸規定，並配合禁菸措施，共同維護清新的賞燈環境。
                </p>

                 <p>
                    觀傳局強調，活動期間將透過現場告示及立牌、廣播、官方網站、社群平台與現場工作人員加強宣導，鼓勵民眾以實際行動支持無菸環境，共同營造健康城市形象。本次燈節亦將由衛生局、環保局、商業處、警察局、觀傳局及相關局處組成跨局處稽查小組，於活動期間每日進行巡查與宣導，針對違規吸菸行為依法勸導及取締。
                </p>

