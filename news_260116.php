                    臺北市政府觀光傳播局今（16）日舉行「2026台北燈節」首場記者會，宣布今年首度推出「雙展區、雙IP」策展模式，橫跨西門展區與花博展區，將於2月25日至3月15日盛大登場。觀傳局局長余祥表示，今年燈節最受矚目的IP聯名亮點，就是花博展區將攜手全球知名IP《變形金剛 TRANSFORMERS》，打造前所未見的主題燈組與光影展演，記者會上更搶先曝光10公尺高的「柯博文主燈」，將為臺北夜晚注入震撼的科幻能量。 
                </p>

                <p>
                    余祥指出，今年台北燈節規劃西門與花博雙展區，花博展區以《變形金剛》為主題，並結合眾多藝術家作品共同佈展。此次合作為《變形金剛》首次於臺灣大型主題燈節進行正式授權燈組設計，極具指標意義。主題燈組將以博派領袖柯博文擔綱主角，並同步登場柯博文戰友大黃蜂燈組，和最大對手密卡登燈組，三大經典角色齊聚花博。其中，柯博文以大型立體裝置燈組，搭配燈光節奏與場域設計，呈現充滿動感與力量感的沉浸式賞燈體驗，預期成為本屆燈節最具話題性的焦點之一。
                </p>

