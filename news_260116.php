                <p>
                    此外，今（16）日也同步公布備受親子族群期待的小提燈。觀傳局表示，2026台北燈節小提燈結合科技感與童趣元素，以柯博文為主題設計製作小提燈，呼應變形金剛的機械風格與城市未來感，兼具收藏價值與互動樂趣，將於燈節期間限量發送，邀請大小朋友一同提燈賞燈，感受節慶氛圍。小提燈將於3月1日至3月3日在西門展區及花博展區發放，並於3月1日至3月2日於各行政區同步發放，相關發放細節仍在規劃中，後續將另行公布發放地點、時間等資訊，請持續關注台北燈節活動官網。
