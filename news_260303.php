                <p>
                臺北市年度最具代表性的節慶盛事「2026台北燈節」熱鬧開展中，臺北市政府觀光傳播局表示，今年首度以「雙展區、雙IP」策展模式，打造「西門展區」與「花博展區」，燈節開展後湧入大量賞燈人潮，開展首周末適逢二二八連假，更有不少民眾全家總動員與好友一起出遊，從2月25日開幕至3月2日六天期間，雙展區共計湧入超過360萬人次參觀。
                </p>

                <p>
                台北燈節今年推出的「柯博文小提燈」也於3月1日下午3時，在西門町、花博公園雙展區同步發送，現場擠滿人潮，不只有小朋友特地帶著模型玩具到場排隊，現場也湧現許多親子家庭與祖孫三代一同前來，民眾拿著柯博文小提燈與燈組合影留念。排隊在第一位的阿嬤開心分享，這次台北燈節行銷到美國去，在美國的孫女和孫子特別打電話叮嚀她一定要來領取小提燈。此外，臺北市12行政區也同步發送柯博文小提燈，吸引大批民眾提前到場排隊等候，活動發送開始後每天約1小時即全數發送完畢，場面相當熱絡，許多小朋友一拿到後就迫不及待組裝，開心提著小提燈到展區賞燈。
                </p>

                <p>
                觀傳局表示，今日是元宵節，因此特別規劃「轉輪盤猜燈謎—馬上轉好運」活動，一同體驗最具傳統年味的節慶樂趣。為了號召民眾為世界棒球經典賽（WBC）中華隊應援，台北燈節3月5日至8日在西門展區《揮出夢想經典熊讚》燈組旁（近中華路1段53號臺北市立國樂團前），設有加油留言牆，邀請民眾到現場為中華隊加油打氣，將熱血傳給全世界。而在花博展區也推出WBC中華隊應援抽獎活動，3月6日18時開始在臺北表演藝術中心戶外賽事轉播現場，民眾可至服務台旁領取台北燈節抽獎券（限量發送，送完為止），3月8日前至台北燈節花博展區賞燈，就可憑券至服務台參加抽獎，有機會獲得iPhone 17、還有台北晶華酒店豪華客房住宿劵等大獎。
                </p>

                

                <p>
                觀傳局預告，接著在白色情人節周末也有「甜蜜印相 × 金莎傳情」活動，3月13日至15日晚間18時至21時浪漫登場，地點在花博展區變形金剛主燈旁，以及西門展區1號服務台旁（近捷運西門站6號出口後方），民眾只要上傳與燈組的兩人合照，即可免費列印燈節限定相片，將燈節浪漫時刻化為實體紀念（每人限1張）。此外，參與者若將現場列印照片上傳至社群平台，並標註 #台北燈節 #情人節快樂，還可獲贈金莎花束（每人限1束，數量有限，送完為止），邀請民眾一起於台北燈節璀璨燈海中留下專屬甜蜜回憶。
