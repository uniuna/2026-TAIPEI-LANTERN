                <p>
                    觀傳局指出，燈節期間從3月1日至3月3日為期3天於花博展區及西門展區發放小提燈，花博展區於服務台發放（花博公園入口花牆）、西門展區則於2號服務台（位於中山堂廣場）發放，每日下午3時開始發放，每人限領1盞，發完為止。另3月1日至3月2日為期2天，臺北市12行政區也將於下午2時開放免費領取小提燈，每人限領1盞，發完為止，12行政區指定發放地點包含：大安區（大安森林公園8號出入口）、內湖區（內湖公民會館）、士林區（士林區行政中心）、文山區（文山行政中心前廣場）、北投區（北投慈后宮）、中山區（中山區行政中心）、信義區（信義區行政中心南側大門）、松山區（松山區行政中心）、萬華區（剝皮寮歷史街區）、中正區（中正紀念堂大孝門旁-臨愛國東路）、大同區（大同區行政中心）、南港區（南港車站CityLink廣場）。
