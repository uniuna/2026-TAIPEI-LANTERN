                    2. 站點「中山堂」YouBike站於燈節期間（2月25日至3月15日）平日17時至22時、假日14時至22時暫停營運。<br>
                    3. 燈節期間中華路人行道及圓山花博公園賞燈人潮眾多，為避免自行車與行人交織發生意外，自行車請下車牽行。
