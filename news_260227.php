                <p>
                    「2026台北燈節」再掀國際交流熱潮，今年友好燈區首度邀請到美國在台協會（AIT）參展，共慶美國建國250周年里程碑，友好燈區集結臺日多位國寶級傳統燈藝大師共同操刀，打造一場盛大的藝術感官盛宴。臺北市政府觀光傳播局表示，多國城市燈組現正於花博展區盛大登場，現場還有許多展攤互動及限定好禮等活動，邀請國內外遊客搭乘捷運至圓山站，即可在璀璨燈海中，感受多國藝術與節慶魅力交織的震撼體驗，親身參與這場馬年限定的文化盛典。
                </p>

                <p>
                    觀傳局表示，台北燈節是臺北接軌世界、與國際交流的最佳平台，今年友好燈區迎來最具指標性的突破，首度參展的美國AIT帶來「輝映自由．共譜繁榮 | Light of Liberty, Promise of Prosperity」燈組作品，與全臺民眾共同慶祝美國建國250周年，也展現臺美友好情誼。本次共有13個國家城市展出特色燈組，除了美國，還有聖克里斯多福及尼維斯、關島及大邱廣域市、上海市，以及日本8個城市（愛媛縣松山市、青森縣弘前市、秋田縣、福島縣、靜岡縣濱松市、島根縣松江市、茨城縣笠間市與和歌山縣），由多組獲獎無數的重量級藝術家、國寶級大師親自操刀。
                </p>

                <p>
                    其中，美國在台協會（AIT）燈組，是由國寶級燈藝師林玉珠領軍，聯手出身花燈世家的工藝師李冠毅、李青芸共同打造；日本青森縣弘前市則特邀知名藝術家川村麗巴（KAWAMURA REIHA）操刀設計鏡畫作品「馬超奮戰圖」與「唐美人」。有「花燈王」美譽的藍永旗花燈藝術工房負責製作秋田縣、福島縣、和歌山縣及靜岡縣濱松市燈組；花燈大師陳祖榮則透過精巧手工為愛媛縣松山市、茨城縣笠間市、大邱廣域市及關島，展現各見特色的異國風情，還有國際級花燈藝術師黃文全為聖克里斯多福及尼維斯打造充滿活力的民俗文化燈組。此外，島根縣松江市燈組，找來吟遊詩人Kota、Pori及Satoshi Tabuchi與臺北市立大學視覺藝術學系學生共同設計，紀念與臺北市「牡丹交流」20周年。
                </p>

                <p>
                    觀傳局說，台北燈節友好燈區推出許多展攤活動，包含聖克里斯多福及尼維斯帶來嘉年華熱情氛圍；青森縣弘前市於指定週末推出「貼紙兌換蘋果」活動，限量2,000張送完為止。臺北姊妹市關島發放查莫洛文化限量帆布購物袋，讓民眾感受海島魅力；大邱廣域市送限量韓國藥果，靜岡縣濱松市則有吉祥物「家康君」親自現身與民眾互動拍照，展現濱松市在地特色。島根縣松江市舉辦填問卷抽獎活動，秋田縣提供限量帆布收納包與手帕；和歌山縣與福島縣則分別贈送限量超細纖維拭鏡布與可愛行李吊牌，分享貼文可參加抽獎拿限量好禮，以及愛媛縣松山市和茨城縣笠間市也有參與社群活動送限量精美小禮物活動等。
                </p>

                <p>
                    「2026台北燈節」花博展區交通便利，捷運圓山站一出站即達展區，即日起展至3月15日，邀請國內外旅客搭乘大眾運輸工具前往賞燈，透過大師作品體驗不同城市的文化風情。更多精彩活動請至活動官網查詢。
                </p>

