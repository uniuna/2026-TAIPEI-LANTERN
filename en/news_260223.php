                <h1 class="article-title">Building a Smoke-Free City - Creating a Fresh Lantern-Viewing Environment<br>Smoking Prohibited in 2026 Taipei Lantern Festival Areas and Surroundings</h1>
                <div class="article-meta">
                    <span>2026.02.23</span>
                    <span>Issuing Agency：Department of Information and Tourism, Taipei City Government</span>
                </div>
            </header>

            <div class="article-body">

                <p>
                    The “2026 Taipei Lantern Festival” will take place from February 25 to March 15 at the Taipei Expo Park Area and Ximen Area. The Department of Information and Tourism, Taipei City Government, stated that smoking - including e-cigarettes and heated tobacco products - is prohibited within the festival areas and surrounding zones. Designated smoking areas have been provided, and violators will face fines ranging from NT$2,000 to NT$10,000. Visitors are reminded to comply with smoking restrictions and help maintain a fresh and enjoyable lantern-viewing environment.
                </p>

                <p>
                    The Department of Information and Tourism explained that in the “Ximen Area,” the smoking ban covers the area bounded by Kaifeng Street to the south, Changsha Street to the north, Xining South Road to the west, and Yanping South Road to the east. This includes pedestrian walkways and arcades at Zhongshan Hall Plaza and the road surface above the Zhonghua Road Footbridge. In the Taipei Expo Park Area, the smoking ban covers Yuanshan Expo Park and its surroundings, extending east to Section 3 of Zhongshan North Road, south to Minzu West Road, west to Linear Park from MRT Yuanshan Station Exit 1, north to Kulun Street from Exit 2, and northward to the lawn recreation area, including all pedestrian pathways.
                </p>

                <p>
                    For visitor convenience, designated smoking areas are available in both festival zones. In the “Ximen Area,” smoking areas are located at the north and south ends of Section 1 of Zhonghua Road, as well as in front of the Emei Parking Structure. In the “Taipei Expo Park Area,” a fixed smoking area within Maji Square is available, and an additional smoking area has been set up at the Zhongshan North Road entrance of Yuanshan Expo Park. Visitors are encouraged to use these designated areas if needed to avoid fines.
                </p>

                 <p>
                    The Department of Information and Tourism clarified the festival hours: February 25 to March 15, Mondays to Fridays from 17:00 to 22:00, weekends and national holidays from 14:00 to 22:00. In addition, the “Taipei Expo Park Area” will extend lighting until 24:00 on Saturdays. Smoking is strictly prohibited throughout the festival zones and surroundings, with violations subject to fines of NT$2,000-10,000. Visitors are reminded to observe these rules and support a smoke-free environment for everyone’s enjoyment.
                </p>

                 <p>
                    The Department emphasized that smoking regulations will be promoted through on-site signage, displays, public announcements, official websites, social media, and on-site staff. Visitors are encouraged to actively support the smoke-free initiative to help foster a healthier city image. A cross-department inspection team - including the Department of Health, Department of Environmental Protection, Taipei City Office of Commerce, Taipei City Police Department, and Department of Information and Tourism - will conduct daily patrols and education, enforcing the smoking ban and guiding visitors according to the law.
                </p>

