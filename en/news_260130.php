                <h1 class="article-title">The 2026 Taipei Lantern Festival Ximen Area Partners with POP MART to Debut Six Popular IPs, Unveil Five Lantern Installations, and Reveal an 8 Meter Baby Molly Main Lantern</h1>
                <div class="article-meta">
                    <span>2026.01.30</span>
                    <span>Issuing Agency：Department of Information and Tourism, Taipei City Government</span>
                </div>
            </header>

            <div class="article-body">
                <img src="../images/news/n260130_01.jpg" alt="新聞配圖">

                <p>
                    For the first time ever, the Taipei Lantern Festival introduces the innovative “Two Zones, Two IPs” concept. On the 29th, the Department of Information and Tourism, Taipei City Government officially revealed that the Ximen Lantern Area will collaborate with the globally popular trend brand POP MART to present lantern installations featuring six of its most beloved IP characters. Blending Ximending’s street culture with contemporary art, the western district of Taipei will transform into a vibrant urban stage where fashion, art, and light converge after dark.
                </p>

                <p>
                    Mayor Wan-an Chiang noted that the Taipei Lantern Festival continues to infuse creativity into this well-loved annual event, offering fresh experiences to the public. This year’s collaboration with POP MART brings six highly popular characters to the Ximen area, including Baby Molly, DIMOO, SKULLPANDA, TWINKLE TWINKLE, Hirono, and LABUBU, a lineup expected to attract massive crowds. The mayor shared with a smile that he even asked his eldest child, Te-li, to give him a crash course on the characters, and expects his younger children, Te-yu and Te-cheng, to eagerly request visits to both exhibition zones once the festival opens on February 25.
                </p>

                <p>
                    Director General Hsiang Yu of the Department of Information and Tourism further explained that this year’s lantern designs are based on POP MART’s latest Year of the Horse themed IP visuals. Five large scale IP lantern installations have been created, integrating Ximending’s surrounding street lighting and nighttime design while incorporating iconic Taipei landmarks. Through collaboration with internationally popular IPs, the festival aims to draw greater crowds and boost tourism value.
                </p>

                <img src="../images/news/n260130_02.jpg" alt="新聞配圖">

                <p> 
                    The Department noted that the Taipei Lantern Festival has long been one of the city’s most important international events. This year marks its first collaboration with POP MART, aimed at drawing more young people and international travelers to the festival and inviting them to experience the unique charm and innovative energy of Taipei’s nighttime culture. As one of Taipei’s most popular tourist destinations, Ximending sees POP MART’s characters seamlessly woven into the city’s streetscape, bringing the Lantern Festival into everyday urban life. Visitors to Ximending can now enjoy not only great food, shopping, and entertainment, but also delightfully eye-catching lantern installations that are perfect for photos and check-ins.
                </p>

                <p> 
                    The Ximen Lantern Area spans The Red House, the surroundings of Ximen MRT Station, and Zhonghua Road Section 1. Lantern installations are arranged along the city’s main axis, allowing visitors to stroll through the district while enjoying light artworks that merge urban imagery with trend-driven art, creating a distinctly metropolitan lantern festival atmosphere.
                </p>

                <p> 
                   The Ximen Lantern Area of the 2026 Taipei Lantern Festival opens on February 26 and runs through March 15, with lights on daily from 5:00 PM to 10:00 PM. Citizens and visitors from around the world are invited to explore Taipei by day and enjoy lantern viewing and photography by night.
                </p>

