                <h3>響應2026台北燈節，邀民眾走進中正區感受夜間街區魅力</h3>

                <p>
                    深受好評的「榮町漫市集」於去年首度亮相即獲熱烈迴響，今年再度於台北市中正區榮町商圈浪漫回歸。第二屆活動響應台北燈節展期自2月25日起至3月15日止，於中山堂前的永綏街盛大展開。市集延續迷人的夜間氛圍，結合特色美食與藝文表演，邀請市民放慢腳步，在燈火與人情味交織的夜晚，感受城市街區的溫柔節奏。
                </p>

                <p>
                    榮町商圈鄰近中山堂，是台北核心地帶重要的歷史文化街區。榮町漫市集自去年起即與台北燈節串連，透過精緻燈飾佈置，讓民眾在賞燈之餘能走進巷弄，體驗不同於大型展區的在地生活風景。今年活動持續響應燈節夜遊氛圍，讓參與者在漫步間感受城市節慶的溫度。
                </p>

                <p>
                    本次市集秉持「慢遊、慢逛、慢生活」核心精神，營造適合停留的街道空間，讓民眾在熱鬧節慶中找到一處舒緩且充滿溫馨感的角落。活動平日開放時間為17時至22時，假日提前於16時開始，非常適合安排夜間散步行程。
                </p>

                <p>市集攤位主打特色美食與在地小吃，嚴選多樣風味餐食進駐，讓遊客能享受邊走邊吃的樂趣。透過燈光點綴與街道佈置，市集營造出與燈節相呼應的視覺美感，讓節慶喜悅在街區延續。</p>

                <img src="images/news/n260213-2_02.jpg" alt="新聞配圖">

                <p>
                    此外，活動期間亦安排豐富舞台演出，演出單位包含太陽視界娛樂領軍15位歌手登上2026榮町商圈漫市集舞台，以流行、爵士與現代創作曲風交織光影藝術，打造沉浸式視聽饗宴，在2026台北燈節中為城市夜晚注入音樂靈魂與春節浪漫氛圍；另臺北市中正社區大學帶來的精彩表演《榮町燈火漫步・樂力國標共舞》，中正社大樂力舞國標於榮町漫市集中登場，展現熱情與優雅交織的舞風層次，邀請民眾在燈火流轉的夜晚邊逛市集、邊欣賞舞蹈，共享榮町的歡樂時光。透過音樂與創意展演，為夜晚的榮町街區注入更多文化與藝術能量，展現社區參與在地藝文活力。
                </p>

                <p>
                    為提升互動，主辦單位同步規劃「迎春新馬燈籠」打卡送燈籠活動，邀請民眾分享市集風景，留下專屬記憶。詳細時程與資訊將陸續公布於 <a href="https://www.facebook.com/Rongtingshoppingarea?locale=zh_TW" target="_blank"">榮町商圈官方Facebook粉絲專頁</a>。
                </p>

                <p>榮町漫市集由臺北市中正區榮町商圈暨城內社區發展協會主辦，期盼透過燈節期間的活動串聯城市夜遊動線，活絡商圈發展，帶領大眾看見中正區夜晚的多元樣貌。</p>

                <p>
                    另外，臺北市中正區公所於3月14日白色情人節，浪漫推出「漢衣相逢，榮町漫遊」單身聯誼活動，邀請單身男女穿扮華麗漢服、漫遊榮町商圈市集及台北舊城歷史街區、品嘗點心美食，並串聯2026台北燈節期間的城市氛圍，打造一場集傳統文化體驗，及互動交流的沉浸式賞燈聯誼活動。歡迎單身男女至 <a href="https://www.accupass.com/event/2601220325271236021465" target="_blank">臺北市中正區公所指定網站</a> 報名。
                </p>

